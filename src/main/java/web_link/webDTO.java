package web_link;

public class webDTO {
	int web_number;
	String web_code;
	String web_name;
	String web_contents;
	String web_url;
	public int getWeb_number() {
		return web_number;
	}
	public void setWeb_number(int web_number) {
		this.web_number = web_number;
	}
	public String getWeb_code() {
		return web_code;
	}
	public void setWeb_code(String web_code) {
		this.web_code = web_code;
	}
	public String getWeb_name() {
		return web_name;
	}
	public void setWeb_name(String web_name) {
		this.web_name = web_name;
	}
	public String getWeb_contents() {
		return web_contents;
	}
	public void setWeb_contents(String web_contents) {
		this.web_contents = web_contents;
	}
	public String getWeb_url() {
		return web_url;
	}
	public void setWeb_url(String web_url) {
		this.web_url = web_url;
	}



}
